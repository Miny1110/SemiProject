package com.ccookat;

public class NoticeDTO {
	
	private int noticeNum;
	private String noticeTitle;
	private String noticeCreated;
	private String noticeContent;
	private int noticeHitCount;
	private String noticeImage;
	
	
	public int getNoticeNum() {
		return noticeNum;
	}
	public void setNoticeNum(int noticeNum) {
		this.noticeNum = noticeNum;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public String getNoticeCreated() {
		return noticeCreated;
	}
	public void setNoticeCreated(String noticeCreated) {
		this.noticeCreated = noticeCreated;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public int getNoticeHitCount() {
		return noticeHitCount;
	}
	public void setNoticeHitCount(int noticeHitCount) {
		this.noticeHitCount = noticeHitCount;
	}
	public String getNoticeImage() {
		return noticeImage;
	}
	public void setNoticeImage(String noticeImage) {
		this.noticeImage = noticeImage;
	}
	
	

}
