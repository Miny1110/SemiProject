package com.ccookat;

public class QnaDTO {

	private int qnaNum;
	private String qnaTitle;
	private String qnaContent;
	private String customerId;
	private String qnaCreated;
	private String qnaAnswer;
	private int qnaHitCount;
	
	
	public int getQnaNum() {
		return qnaNum;
	}
	public void setQnaNum(int qnaNum) {
		this.qnaNum = qnaNum;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQnaContent() {
		return qnaContent;
	}
	public void setQnaContent(String qnaContent) {
		this.qnaContent = qnaContent;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getQnaCreated() {
		return qnaCreated;
	}
	public void setQnaCreated(String qnaCreated) {
		this.qnaCreated = qnaCreated;
	}
	public String getQnaAnswer() {
		return qnaAnswer;
	}
	public void setQnaAnswer(String qnaAnswer) {
		this.qnaAnswer = qnaAnswer;
	}
	public int getQnaHitCount() {
		return qnaHitCount;
	}
	public void setQnaHitCount(int qnaHitCount) {
		this.qnaHitCount = qnaHitCount;
	}
	
	
	
}
