package com.ccookat;

public class CustomerDTO {

	private String customerId;
	private String customerPwd;
	private String customerName;
	private String customerTel;
	private String customerZipcode;
	private String customerAddr;
	private String customerCreated;
	private String customerYear;
	private String customerMonth;
	private String customerDay;
	
	
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getCustomerPwd() {
		return customerPwd;
	}
	public void setCustomerPwd(String customerPwd) {
		this.customerPwd = customerPwd;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerTel() {
		return customerTel;
	}
	public void setCustomerTel(String customerTel) {
		this.customerTel = customerTel;
	}
	public String getCustomerZipcode() {
		return customerZipcode;
	}
	public void setCustomerZipcode(String customerZipcode) {
		this.customerZipcode = customerZipcode;
	}
	public String getCustomerAddr() {
		return customerAddr;
	}
	public void setCustomerAddr(String customerAddr) {
		this.customerAddr = customerAddr;
	}
	public String getCustomerCreated() {
		return customerCreated;
	}
	public void setCustomerCreated(String customerCreated) {
		this.customerCreated = customerCreated;
	}
	public String getCustomerYear() {
		return customerYear;
	}
	public void setCustomerYear(String customerYear) {
		this.customerYear = customerYear;
	}
	public String getCustomerMonth() {
		return customerMonth;
	}
	public void setCustomerMonth(String customerMonth) {
		this.customerMonth = customerMonth;
	}
	public String getCustomerDay() {
		return customerDay;
	}
	public void setCustomerDay(String customerDay) {
		this.customerDay = customerDay;
	}
	
	
	
}
